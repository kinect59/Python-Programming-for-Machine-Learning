# Comparison two numbers and these returns.
5 < 3                                     # Returns False
5 > 3                                     # Returns True
5 == 5                                    # Returns True
5 != 2                                    # Returns True
5 <= 2                                    # Returns False
5 >= 2                                    # Returns True

# Boolean operations.
5 > 3 and 4 > 2                           # Returns True
5 > 3 or 1 < 0                            # Returns True



