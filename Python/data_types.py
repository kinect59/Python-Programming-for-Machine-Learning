# Return the type of an object.
type(2)           # Return <class 'int'>
type(2.0)         # Return <class 'float'>
type('string')    # Return <class 'str'>
type("string")    # Return <class 'str'>
type(True)        # Return <class 'bool'>
type(None)        # Return <class 'NoneType'>

# Convert an object to a given type.
int(1.2)          # Return 1
float(2)          # Return 2.0
str(1.5)          # Return '1.5'
